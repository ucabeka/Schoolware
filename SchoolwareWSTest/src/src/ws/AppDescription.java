package src.ws;

public class AppDescription {
	
	String description, appName, url, iconUrl, developerName, appSize;
	long appCategId, appId, appTypeId;
	
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getAppName() {
		return appName;
	}
	public void setAppName(String appName) {
		this.appName = appName;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getIconUrl() {
		return iconUrl;
	}
	public void setIconUrl(String iconUrl) {
		this.iconUrl = iconUrl;
	}
	public String getDeveloperName() {
		return developerName;
	}
	public void setDeveloperName(String developerName) {
		this.developerName = developerName;
	}
	public String getAppSize() {
		return appSize;
	}
	public void setAppSize(String appSize) {
		this.appSize = appSize;
	}
	public long getAppCategId() {
		return appCategId;
	}
	public void setAppCategId(long appCategId) {
		this.appCategId = appCategId;
	}
	public long getAppId() {
		return appId;
	}
	public void setAppId(long appId) {
		this.appId = appId;
	}
	public long getAppTypeId() {
		return appTypeId;
	}
	public void setAppTypeId(long appTypeId) {
		this.appTypeId = appTypeId;
	}
	
	
	

}
